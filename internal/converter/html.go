// reAlertBlockquote matches goldmark's HTML output for GitHub-style alert blockquotes.
// Goldmark renders `> [!TYPE]\n> content` as:
//
//	<blockquote>\n<p>[!TYPE]<br />\ncontent</p>\n</blockquote>
//
// This regex captures the alert type (NOTE, WARNING, etc.) and body content.
var reAlertBlockquote = regexp.MustCompile(
	`(?s)<blockquote>\s*<p>\[!(NOTE|WARNING|TIP|CAUTION|IMPORTANT)\]` +
		`(?:<br\s*/?>)?\s*\n?(.*?)</p>\s*</blockquote>`,
)

// alertSubmatchLen is the minimum submatch length for alert regex matches
// (full match + alert type + body content).
const alertSubmatchLen = 3

// alertTitles maps alert type identifiers to their display titles.
var alertTitles = map[string]string{
	"NOTE":      "Note",
	"WARNING":   "Warning",
	"TIP":       "Tip",
	"CAUTION":   "Caution",
	"IMPORTANT": "Important",
}

// goldmarkRenderer is a pre-configured goldmark instance shared between:
//   - HTML output (renderMarkdownToHTML)
//   - Plain text output (stripMarkdownFormatting in plaintext.go)
//
// Configuration matches internal/markdown/formatters.go:RenderMarkdown().
//
// WithUnsafe is required because the markdown builder emits raw HTML tags
// (<details>, <summary>) that must pass through to the final output.
// This is safe here: the input is programmatically generated by our own
// builder, not user-supplied content. See also internal/diff/formatters/html.go.
var goldmarkRenderer = goldmark.New(
	goldmark.WithExtensions(extension.Table),
	goldmark.WithParserOptions(
		goldmark_parser.WithAutoHeadingID(),
	),
	goldmark.WithRendererOptions(
		html.WithHardWraps(),
		html.WithXHTML(),
		html.WithUnsafe(), // safe: input is builder-generated, not user content
	),
)

// transformAlertBlockquotes converts goldmark blockquote output containing
// GitHub-style alert markers ([!NOTE], [!WARNING], etc.) into styled HTML
// div elements. Non-alert blockquotes are left unchanged.
func transformAlertBlockquotes(htmlContent string) string {
	return reAlertBlockquote.ReplaceAllStringFunc(htmlContent, func(match string) string {
		submatch := reAlertBlockquote.FindStringSubmatch(match)
		if len(submatch) < alertSubmatchLen {
			return match
		}

		alertType := submatch[1]
		body := strings.TrimSpace(submatch[2])
		typeLower := strings.ToLower(alertType)

		title, ok := alertTitles[alertType]
		if !ok {
			return match
		}

		return fmt.Sprintf(
			"<div class=\"alert alert-%s\">\n<p class=\"alert-title\">%s</p>\n<p>%s</p>\n</div>",
			typeLower, title, body,
		)
	})
}

// renderMarkdownToHTML converts markdown content to a self-contained HTML document.
// It uses goldmark for markdown-to-HTML conversion, transforms GitHub-style alert
// blockquotes into styled div elements, and wraps the result in htmlShell.
func renderMarkdownToHTML(md string) (string, error) {
	var buf strings.Builder
	if err := goldmarkRenderer.Convert([]byte(md), &buf); err != nil {
		return "", fmt.Errorf("failed to convert markdown to HTML: %w", err)
	}

	body := transformAlertBlockquotes(buf.String())

	return fmt.Sprintf(htmlShell, body), nil
}
